const DOMAIN_ALIASES = [
  {
    domain: "azure.com",
    hosts: ["azure.microsoft.com"],
    contains: ["azure.com", "azure.microsoft.com", "/azure"],
    titlePattern: /(^|[^a-z0-9])azure([^a-z0-9]|$)/i,
  },
];

export function normalizeHostname(value = "") {
  const raw = String(value || "").trim().toLowerCase();
  if (!raw) return "";

  try {
    const withProtocol = /^https?:\/\//i.test(raw) ? raw : `https://${raw}`;
    const parsed = new URL(withProtocol);
    return parsed.hostname.replace(/^www\./, "");
  } catch {
    return raw
      .replace(/^https?:\/\//i, "")
      .split(/[/?#]/)[0]
      .replace(/^www\./, "");
  }
}

function getReportHostname(report = {}) {
  const fromHost = normalizeHostname(report.host);
  if (fromHost) return fromHost;

  const fromUrl = normalizeHostname(report.url);
  if (fromUrl) return fromUrl;

  return "";
}

function isSameDomainOrSubdomain(hostname, serviceDomain) {
  if (!hostname || !serviceDomain) return false;
  return hostname === serviceDomain || hostname.endsWith(`.${serviceDomain}`);
}

function getAliasDomain(report = {}, hostname = "") {
  const host = normalizeHostname(hostname);
  const url = String(report?.url || "").toLowerCase();
  const policyUrl = String(report?.policyUrl || "").toLowerCase();
  const pageTitle = String(report?.pageTitle || "").toLowerCase();
  const rawHost = String(report?.host || "").toLowerCase();
  const combined = `${rawHost} ${host} ${url} ${policyUrl} ${pageTitle}`;

  for (const alias of DOMAIN_ALIASES) {
    const hostMatches = (alias.hosts || []).some((aliasHost) => isSameDomainOrSubdomain(host, aliasHost));
    const contentMatches = (alias.contains || []).some((needle) => combined.includes(needle));
    const titleMatches = alias.titlePattern ? alias.titlePattern.test(pageTitle) : false;
    if (hostMatches || contentMatches || titleMatches) return alias.domain;
  }

  return "";
}

function slugify(value = "") {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

export function buildCatalogServiceSlugFromService(service = {}) {
  if (!service?.name || !service?.domain) return "";
  return slugify(`${service.name}-${service.domain}`);
}

export function colorFromCatalogGrade(grade) {
  return COLOR_BY_GRADE[grade] || COLOR_BY_GRADE.DEFAULT;
}

export function findCatalogServiceForReport(report = {}) {
  const hostname = getReportHostname(report);
  const aliasDomain = getAliasDomain(report, hostname);

  if (aliasDomain) {
    const aliasMatch = CATALOG_SCORE_SERVICES.find((service) => normalizeHostname(service.domain) === aliasDomain);
    if (aliasMatch) return aliasMatch;
  }

  if (!hostname) return null;

  const sortedBySpecificDomain = [...CATALOG_SCORE_SERVICES].sort((a, b) => {
    return normalizeHostname(b.domain).length - normalizeHostname(a.domain).length;
  });

  return sortedBySpecificDomain.find((service) => {
    return isSameDomainOrSubdomain(hostname, normalizeHostname(service.domain));
  }) || null;
}

export function applyCatalogScore(scored = {}, report = {}) {
  const catalogService = findCatalogServiceForReport(report);
  if (!catalogService) {
    return { ...scored, catalogMatch: null, scoreSource: "live-analysis" };
  }

  return {
    ...scored,
    score: Number(catalogService.score),
    grade: catalogService.rating,
    colorHex: colorFromCatalogGrade(catalogService.rating),
    catalogMatch: {
      name: catalogService.name,
      domain: catalogService.domain,
      rating: catalogService.rating,
      score: Number(catalogService.score),
      slug: buildCatalogServiceSlugFromService(catalogService),
    },
    scoreSource: "frontend-catalog",
  };
}
